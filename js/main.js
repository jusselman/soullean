// CHANGE NAVBAR WHEN SCROLLED DOWN TO NEW SECTIONS //
var home = $('#home').offset().top;
var about = $('#about').offset().top;
var menu = $('#menu').offset().top;
var events = $('#events').offset().top;
var contact = $('#contact').offset().top;

$(document).scroll(function() {
  var scrollPos = $(document).scrollTop();
  if (scrollPos >= home && scrollPos < about) {
    $('#change').css('background-color', '#f00');
  } else if (scrollPos >= about && scrollPos < menu) {
    $('#change').css('background-color', '#0f0');
  } else if (scrollPos >= menu && scrollPos < events) {
    $('#change').css('background-color', '#00f');
  } else if (scrollPos >= events && scrollPos < contact) {
    $('#change').css('background-color', '#00f');
  }
});
